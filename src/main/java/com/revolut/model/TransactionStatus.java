package com.revolut.model;

/**
 * The enum Transaction status.
 */
public enum TransactionStatus {
    /**
     * New transaction status.
     */
    NEW,
    /**
     * In progress transaction status.
     */
    IN_PROGRESS,
    /**
     * Completed transaction status.
     */
    COMPLETED,

    /**
     * Error transaction status.
     */
    ERROR
}
